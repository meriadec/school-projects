#include "draw.h"

void						ft_draw(t_ctx *ctx)
{
	int						x;
	int						y;

	y = 0;
	while (y < H)
	{
		x = 0;
		while (x < W)
		{
			ft_draw_cell(CUR, x, y);
			++x;
		}
		++y;
	}
}
