#include "libft.h"

void				ft_free_tab(void ***arr)
{
	int				i;

	i = 0;
	while ((*arr)[i])
	{
		ft_memdel((void **) &(*arr)[i]);
		++i;
	}
	ft_memdel((void **) arr);
}
