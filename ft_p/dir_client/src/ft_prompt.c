#include "client.h"

void						ft_prompt(void)
{
	t_ctx					*ctx;

	ctx = ft_ctx();
	ft_putstr(PROMPT);
	ft_putstr(" [\033[0;32m");
	ft_putstr(ctx->pwd);
	ft_putstr("\033[0m]> ");
}
