#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int		length;
	int		i;

	if (s && f)
	{
		length = ft_strlen(s);
		i = 0;
		while (i < length)
		{
			f(&(s[i]));
			i++;
		}
	}
}
